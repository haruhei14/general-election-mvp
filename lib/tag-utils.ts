// 読み仮名を取得するヘルパー関数
export function getReading(tag: string): string {
    // マッピングにあればそれを使用、なければ元のタグをひらがなに変換（ベストエフォート）
    // カタカナはひらがなに変換
    const reading = TAG_READINGS[tag] || tag;
    return reading.replace(/[\u30a1-\u30f6]/g, function (match) {
        var chr = match.charCodeAt(0) - 0x60;
        return String.fromCharCode(chr);
    }).toLowerCase(); // アルファベットは小文字に
}

// 50音の行を取得する関数
export function getGroupChar(reading: string): string {
    const firstChar = reading.charAt(0);

    // ひらがなの範囲判定
    if (/[あ-お]/.test(firstChar)) return 'あ行';
    if (/[か-ご]/.test(firstChar)) return 'か行';
    if (/[さ-ぞ]/.test(firstChar)) return 'さ行';
    if (/[た-ど]/.test(firstChar)) return 'た行';
    if (/[な-の]/.test(firstChar)) return 'な行';
    if (/[は-ぽ]/.test(firstChar)) return 'は行';
    if (/[ま-も]/.test(firstChar)) return 'ま行';
    if (/[や-よ]/.test(firstChar)) return 'や行';
    if (/[ら-ろ]/.test(firstChar)) return 'ら行';
    if (/[わ-ん]/.test(firstChar)) return 'わ行';

    // その他（アルファベットなど）
    return 'その他';
}
